    private void searchNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchNameActionPerformed
  
        try{
        String sql = "select * from stockinventory where productname =?";
        
        pst = conn.prepareStatement(sql);
        pst.setString(1,search_tf.getText());
        
        rs = pst.executeQuery();
        
       
   /** if(rs.next()==false){
        JOptionPane.showMessageDialog(null, "no results");
    } else{ */
        if(rs.next()){
            String add1 = rs.getString("productname");
            productN_tf.setText(add1);
            String add2 = rs.getString("productID");
            productID_tf.setText(add2);
            String add3 = rs.getString("quantityA");
            quantityAv_tf.setText(add3);
            String add4= rs.getString("Price");
            price_tf.setText(add4);
            String add5 = rs.getString("GST");
            gst_tf.setText(add5);
            String add6 = rs.getString("HSNcode");
            hsn_tf.setText(add6);
            
            
             
        }          
           
            } catch(Exception e){
JOptionPane.showMessageDialog(null, e);
        
                }finally{
                   try{
                       rs.close();
                       pst.close();
                   }catch(Exception e){
                       JOptionPane.showMessageDialog(null,e);
                   }
               }
    }//GEN-LAST:event_searchNameActionPerformed

