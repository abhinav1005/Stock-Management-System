    public void Product_id(){
        try{
         String url = "jdbc:sqlite:C:\\Users\\Abhinav\\Desktop\\apple.db";
         conn= DriverManager.getConnection(url);
            String sql = "select max(productID) from stockinventory";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            
            if(rs.next()){
               int old_id = rs.getInt(1);
               int new_id = old_id + 1;
               String ab = Integer.toString(new_id);
               productID_tf.setText(ab);
              
               rs.close();
               pst.close();
            }
        }catch(Exception e){
        
        JOptionPane.showMessageDialog(null,e);
        
        }finally{
            try{
                rs.close();
                pst.close();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }
